// Identifier, Literal, Operator
var a = 10;
console.log(a);

var b = 20;
console.log(b);

// a, b are identifiers, 10 and 20 are literals, = is operator



// Comments, Identifier, Literal, Operator

// Rule of Identifier:
// 1. Must start with a letter, underscore, or dollar sign
// 2. Can contain letters, numbers, underscores, and dollar signs
// 3. Cannot start with a number
// 4. Cannot be a reserved keyword
// 5. Cannot contain spaces
// 6. Cannot contain special characters other than underscore and dollar sign
// 7. Case sensitive


var name = "Playwright";
console.log(name);

var $name = "$Playwright";
console.log($name);

var _name = "_Playwright";
console.log(_name);

var name1 = "Playwright1";
console.log(name1);

var name_1 = "Playwright_1";
console.log(name_1);

var name$1 = "Playwright$1";
console.log(name$1);

var name_1$1 = "Playwright_1$1";
console.log(name_1$1);

var name_1$1_2 = "Playwright_1$1_2";
console.log(name_1$1_2);

var NAME = "Upender Reddy Thudi";
var name1 = "Thudi Upender Reddy";
console.log(NAME);
console.log(name1);

