// Breakfast MON - FRI using Switch Case

let day = 2;

switch(day){
    case 1:
        console.log(day, ' - Sunday - Bonda');
        break;
    case 2:
        console.log(day, ' - Monday - Upma');
        break;
    case 3:
        console.log(day, ' - Tuesday - Idly');
        break;
    case 4:
        console.log(day, ' - Wednesday - Poha ')
        break;
    case 5:
        console.log(day, ' - Thursday - Dosa ')
        break;
    case 6:
        console.log(day, ' - Friday - Puri ')
        break;
    case 7:
        console.log(day, ' - Satuarday - Chapati ')
        break;
    default:
        console.log(day, ' - Invalid Day ')     
}