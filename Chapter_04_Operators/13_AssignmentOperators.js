//Assignment Operators
let x = 5;
x += 5; // x = x + 5
console.log('x + 5 = ' + x); // Output: 10 - 5 plus 5 is 10

let y = 10;
y -= 5; // y = y - 5
console.log('y - 5 = ' + y); // output: 5 - 10 minus 5 is 5

let z = 15;
z *= 2; //z = z * 2
console.log('z * 2 = ' + z); // output: 30 - 15 multiplied by 2 is 30

let a = 20;
a /= 4; // a = a/4
console.log('a / 4 = ' + a); // output: 5 - 20 divided by 4 Quotient is 5

let b = 10;
b %= 2; //b = b % 2
console.log('b % 2 = ' + b); // output: 0 - 10 divided by 2 leaves a remainder of 0

let c = 5;
c **= 3; //c = c ** 3
console.log('c ** 3 = ' + c); // output: 125 - 5 to the power of 3 is 125